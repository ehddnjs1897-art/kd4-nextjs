-- 연령대(age_group) 버킷 세분화 — '50대 이상' → '50대' / '60대 이상' 분리
-- 배경: 대표 지시로 배우 DB 필터에 40·50·60대+ 버튼 노출. 기존 CHECK 제약이
--       '20대','30대','40대','50대 이상' 4종만 허용 → 신규 '50대'/'60대 이상' 저장 시 23514로 거부.
-- 이 마이그레이션이 제약을 확장한다. 멱등(재실행 안전).
--
-- ⚠️ 적용 순서/안전성:
--   · 코드(온보딩 derivation)는 이미 '50대'/'60대 이상'을 만들되, 제약 미적용 구간에서는
--     레거시 '50대 이상'으로 자동 폴백(23514 가드) → 이 SQL 적용 전에도 접수 500 없음.
--   · 기존 8명(이기옥·장서후·박성만·이도경·정애란·전금주·방성준·이훈)은 birth_year가 없어
--     자동으로 50대/60대 구분 불가 → '50대 이상' 레거시 값 그대로 유지한다.
--     배우 목록의 '50대' 버튼은 '50대 이상'도 함께 노출하므로 이들은 계속 보인다.
--     실제 60대+ 인 멤버는 추후 대표가 birth_year 입력 또는 관리자에서 '60대 이상'으로 조정.
--
-- 적용: Supabase 대시보드 → SQL Editor → 아래 전체 붙여넣기 → Run.

ALTER TABLE actors DROP CONSTRAINT IF EXISTS actors_age_group_check;

ALTER TABLE actors
  ADD CONSTRAINT actors_age_group_check
  CHECK (age_group IN ('20대', '30대', '40대', '50대', '60대 이상', '50대 이상'));
  -- '50대 이상'은 레거시(제약 확장 전 데이터) 호환용. 신규 접수는 '50대'/'60대 이상'으로 저장됨.

COMMENT ON COLUMN actors.age_group IS '연령대 버킷: 20대/30대/40대/50대/60대 이상 (+ 레거시 50대 이상)';
